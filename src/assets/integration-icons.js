// Map integration values to their category icons
export const getIconForIntegration = (value) => {
  // AWS services
  if (
    value.startsWith("aws_") ||
    value.startsWith("amazon_") ||
    value === "aws"
  ) {
    return integrationIcons.aws;
  }

  // Azure services
  if (value.includes("azure") || value.startsWith("microsoft_")) {
    return integrationIcons.azure;
  }

  // GCP services
  if (
    value.startsWith("gcp") ||
    value.includes("google_cloud") ||
    value.includes("gcloud")
  ) {
    return integrationIcons.gcp;
  }

  // Security related
  if (
    value.includes("security") ||
    value.includes("firewall") ||
    value.includes("threat") ||
    value.includes("waf") ||
    value.includes("secure") ||
    value.includes("sentinel") ||
    value.includes("defender") ||
    value.includes("protect") ||
    value.includes("guard")
  ) {
    return integrationIcons.security;
  }

  // Database systems
  if (
    value.includes("sql") ||
    value.includes("db") ||
    value.includes("mongo") ||
    value.includes("redis") ||
    value.includes("postgres") ||
    value.includes("mysql") ||
    value.includes("cassandra") ||
    value.includes("oracle") ||
    value.includes("couchdb")
  ) {
    return integrationIcons.database;
  }

  // Network related
  if (
    value.includes("network") ||
    value.includes("cisco") ||
    value.includes("dns") ||
    value.includes("traffic") ||
    value.includes("flow") ||
    value.includes("vpc") ||
    value.includes("proxy") ||
    value.includes("netflow")
  ) {
    return integrationIcons.network;
  }

  // OS related
  if (
    value.includes("windows") ||
    value.includes("linux") ||
    value.includes("ubuntu") ||
    value.includes("mac") ||
    value.includes("os") ||
    value === "system"
  ) {
    return integrationIcons.os;
  }

  // Web related
  if (
    value.includes("http") ||
    value.includes("web") ||
    value.includes("apache") ||
    value.includes("nginx") ||
    value.includes("iis") ||
    value.includes("traefik") ||
    value.includes("cloudflare")
  ) {
    return integrationIcons.web;
  }

  // Monitoring related
  if (
    value.includes("monitor") ||
    value.includes("metrics") ||
    value.includes("observ") ||
    value.includes("apm") ||
    value.includes("log") ||
    value.includes("trace")
  ) {
    return integrationIcons.monitoring;
  }

  // Default
  return integrationIcons.default;
};
